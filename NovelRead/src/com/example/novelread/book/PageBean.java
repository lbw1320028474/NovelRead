package com.example.novelread.book;


public class PageBean {
	private BitmapBean prePageBitmap = null;
	private BitmapBean currentPageBitmap = null;
	private BitmapBean nextPageBitmap = null;
	public BitmapBean getPrePageBitmap() {
		return prePageBitmap;
	}
	public void setPrePageBitmap(BitmapBean prePageBitmap) {
		this.prePageBitmap = prePageBitmap;
	}
	public BitmapBean getCurrentPageBitmap() {
		return currentPageBitmap;
	}
	public void setCurrentPageBitmap(BitmapBean currentPageBitmap) {
		this.currentPageBitmap = currentPageBitmap;
	}
	public BitmapBean getNextPageBitmap() {
		return nextPageBitmap;
	}
	public void setNextPageBitmap(BitmapBean nextPageBitmap) {
		this.nextPageBitmap = nextPageBitmap;
	}
	
	
}
